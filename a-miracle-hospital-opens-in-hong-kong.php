<p>Perched on a Hong Kong hill is a brand-new 25-story hospital building that Seventh-day Adventist Church leaders call nothing short of a miracle.</p>
<p>The US$219 million building, which officially opened Tuesday, will significantly expand Adventist health-care services in a secular megacity where the church has found that the most effective way to share Jesus’ love is through the healing work of its two local hospitals.</p>
<p>“God brought the right people to the right positions at the right time to make sure this project was successful,” said Robert Folkenberg Jr., chairman of the two hospitals and president of the Adventist Church’s Chinese Union Mission.</p>
<p>The new building is an expansion of Hong Kong Adventist Hospital - Tsuen Wan, which has been housed in a neighboring five-story building since its founding by prominent Adventist doctor Harry Miller in 1964.</p>
<p>The hospital, which got off to a rocky start, has flourished over the past decade, especially for a period of about seven years when the Hong Kong government allowed mothers from mainland China to have their babies in Hong Kong.</p>
<p>“This open door allowed our hospitals, whose maternity wards were immediately flooded, to minister to thousands of families from China while at the same time bringing in much-needed revenue that provided the considerable funds to build this massive expansion,” Folkenberg said.</p>
<p>Income from other patient services and donations also paid for a large portion of the new building’s price tag of 1.7 billion Hong Kong dollars, while a fourth of the cost was covered by a bank loan.</p>
<p>Hong Kong Adventist Hospital - Tsuen Wan is the only private hospital serving a district of 1.5 million people, and the expansion will allow it to care for 1,000 outpatients a day from its previous 300 to 400. The number of beds will increase from 120 to 470, with 20 percent of the beds set aside for low-income patients referred from public hospitals.</p>

<h3>A Model in Private Healthcare</h3>
<p>Hong Kong’s top health official, Ko Wing-man, praised the expansion as a way provide higher quality healthcare in a city that needs more private hospitals.</p>
<p>“Hong Kong is facing an unprecedented challenge in terms of our healthcare system,” he told 400 guests gathered in a grassy garden area on the new building’s sixth floor for an opening ceremony. “We are facing a rapidly aging population … and an escalation in people’s expectations for the quality of medical services.”</p>
<p>The hospital aims to do much more than treat the ill. A major focus is on preventative medicine, helping people live healthily through improved diet, rest, and exercise. Speakers at the opening ceremony underscored public health statistics that show a majority of illnesses are caused by non-communicative diseases linked to poor lifestyle choices.</p>
<p>“It is gratifying to note that this great new initiative — the rebuilding and retooling of the Hong Kong Adventist Hospital - Tsuen Wan — not only includes a state-of-the-art facility for diagnosing and treating diseases but for also creating an environment to promote health — to create a healthy city, if you please!” said Peter N. Landless, director of the Adventist world church’s Health Ministries department. “This approach embodies the philosophy of health germane to the Seventh-day Adventist Church since its earliest fledgling days in 1863.”</p>
<p>Ella Simmons, general vice president of the Adventist world church, reminded guests that Anselm Hennis, director of the department of non-communicable diseases and mental health at the Pan-American Health Organization, appealed to the Adventist Church last year to share its expertise on healthy lifestyles with the rest of the world.</p>
<p>“We must do more, and this is one step in that direction,” she said.</p>

<h3>A Hospital Tour</h3>
<p>Hospital staff members were clearly excited about the expansion, and they led guests on tours of hospital floors festively decorated with pink ribbons and pink balloons. Pink is the hospital’s signature color.</p>
<p>The wide corridors and spacious patient rooms made the floor look more like a hotel than a typical hospital. A tour of the pediatrics unit found private, semi-private, and standard three-bed wards equipped with flat-screen television sets, toys, and playful wall designs. Trays containing attractive meals of fruit, yogurt, and soy meat were placed beside the beds.</p>
<p>The hospital only serves vegetarian meals, and they are prepared by hotel chefs, hospital officials said.</p>
<p>A children’s lounge area in the pediatrics unit was filled with child-sized chairs, a bookcase of children’s books, and a television. The head nurse said a chaplain would read Bible stories to children in the room.</p>
<p>Chaplain visits are actually a trademark of the hospital. Each patient receives a visit from a chaplain during his or her stay, hospital officials said.</p>
<p>Construction of the 607,213-square-foot (56,412-square-meter) building started in 2011 and is scheduled for completion in 2016. In addition to the wards, the building features 10 operating theaters, four endoscopy suites, and the sky garden where the opening ceremony was held. The sky garden offers grass and leafy plants where patients and staff can relax or eat picnic lunches outdoors. The cafeteria is located on the same floor.</p>
<p>“Fully accessible to patients and staff, the open sky garden not only improves ventilation and creates a greening effect, but also offers a sanctuary for relaxation and tranquility,” the hospital said in a statement.</p>

<h3>The Legacy of Harry Miller</h3>
<p>The expanded hospital promises to cement the legacy of Harry Miller, a U.S. physician known as the “China doctor” for his decades of work in mainland China. at the age of 80, Miller accepted a request by the Adventist Church to establish a hospital in Hong Kong in the 1960s. He saw a lack of health-care services in Tsuen Wan, at the time a small coastal town, and decided to build a hospital. The project was difficult and slowly progressed with help from the church, donors, and the Hong Kong and U.S. governments.</p>
<p>“With selfless dedication and extraordinary perseverance, he accomplished the establishment of two hospitals in the city within the following two decades,” the hospital statement said.</p>
<p>The other hospital, located on the other side of town, opened in the early 1970s.</p>
<p>The first patients at the Tsuen Wan hospital were sampan dwellers, factory workers, and villagers living in squatter huts.</p>
<p>“Patients were offered affordable healthcare regardless of whether they were rich or poor,” the hospital said. “The hospital relied on charitable donations.”</p>
<p>Today, the hospital is part of the Adventist Health network of 170 hospitals and 430 medical institutions around the world.</p>
<p>Folkenberg said the newly expanded hospital would shine as a beacon of Christ’s healing love in a crowded metropolis known for having more buildings above 115 feet (35 meters) and more skyscrapers above 490 feet (150 meters) than any other city in the world. The new hospital building stands at 360 feet (110 meters) high.</p>
<p>“We have a very good reputation,” Folkenberg said. “People know the Adventist Church because of our hospitals.”</p>
<p>But only some 7 percent of the city’s 7.1 million residents are Christian.</p>
<p>“In the middle of the city with the most skyscrapers in the world, at the center of an urban, secular, materialistic megacity, we continue to provide excellent, Christ-centered healing ministry to this non-Christian population,” Folkenberg said. “As our motto says, we exist ‘to extend the healing ministry of Jesus.’”</p>

