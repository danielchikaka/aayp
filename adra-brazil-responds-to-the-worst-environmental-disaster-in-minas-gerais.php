<p>The Adventist Development and Relief Agency (ADRA) in Brazil has mobilized efforts to help meet the needs of more than a quarter of a million people in the Brazilian state of Minas Gerais who are without drinking water as the state continues to face what national leaders are calling an “environmental catastrophe.”     </p>                                

<p>On November 10, two dams operated by a private mining company failed, causing a massive toxic mudslide of orange sediment to go into the Rio Doce river and eventually make its way to the Atlantic Ocean. The toxic components in the mudslide, including arsenic, zinc, copper and mercury, have also polluted the water supply in the region, and have made the water untreatable. </p>

<p>The mudslide has also killed 9 people, left 19 missing, and displaced 500. </p>

<p>Immediately after the disaster, ADRA Brazil complemented the support offered by the national government and the mining company by distributing hygiene kits, which included soap, shampoo, toilet paper, disposable diapers and more, to 600 families. </p>

<p>This is the time for the Church to show solidarity and compassion for people, no matter who they are,” said Fabio Salles, director of ADRA Brazil. </p>

<p>As the immediate needs of residents became more apparent with the ongoing flow of toxic mud, Seventh-day Adventist Adventist groups and institutions, including ADRA Brazil, pathfinder clubs and Adventist schools, have come up with various ways to collect and distribute water to families in need.</p>

<p>ADRA Brazil is aiming to distribute 200,000 liters of water to 1,900 families in the city of Governador Valadares and 60,000 liters of water to 570 families in the city of Colatina. </p>

<p>The “Almirante Tamandaré” pathfinder club from the Central Sierra Seventh-day Adventist Church, has organized a bottled water drive with the goal of collecting more than 1,000 liters of water, according to the club’s director, Tarcisio Goese.</p>


<p>
The Barra de São Francisco Adventist School has become a collection base for bottled water, drawing contributions from other Adventist schools in the region.</p>

<p>At this point it is important to join forces to help those who are being deprived of a basic need,” said Luciana Ritter, principal of Barra de São Francisco Adventist School. “Especially families who have children.”</p>

